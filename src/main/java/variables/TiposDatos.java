package variables;

public class TiposDatos {
    public static void main(String[] args) {

        // Números enteros
        byte b = 8;
        short peque = 123;
        int entero = 123456789;
        long grande = 123123123123123l;
        long otroGrande = 123_456_789_123_345l;

        // Números con decimales
        float decimalPeque = 123.456f;
        double decimalGrande = 12423058237096827039468.123123123;
        double notacionCientifica = 1.12434234e-10;

        // Booleanos
        boolean condicion = true;
        boolean condicionDos = false;
        
        // Caracteres y cadenas
        char letra = 'A';
        String nombre = "Antonio";
    }
}
